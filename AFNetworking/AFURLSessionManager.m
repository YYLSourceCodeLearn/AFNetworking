static dispatch_queue_t url_session_manager_creation_queue() {
    static dispatch_queue_t af_url_session_manager_creation_queue;
    static dispatch_once_t onceToken;
    //保证了即使在多线程环境下 也不会创建其它队列
    dispatch_once(&onceToken, ^{
        af_url_session_manager_creation_queue = dispatch_queue_create("com.alamofire.networking.session.manager.creation", DISPATCH_QUEUE_SERIAL);
    });

    return af_url_session_manager_creation_queue;
}

static void url_session_manager_create_task_safely(dispatch_block_t block) {
    if (NSFoundationVersionNumber < NSFoundationVersionNumber_With_Fixed_5871104061079552_bug) {
        // Fix of bug
        // Open Radar:http://openradar.appspot.com/radar?id=5871104061079552 (status: Fixed in iOS8)
        // Issue about:https://github.com/AFNetworking/AFNetworking/issues/2093
        //理解一下
        //第一为什么用 sync，因为是想要主线程等在这，等执行完，再返回，因为执行完 dataTask 才有数据传值才有意义。
        //第二，为什么要用串行队列，因为这块是为了防止 iOS8以下内部的 dataTaskWithRequest 是并发创建的
        //这样会导致taskIdentifiers 这个属性值不唯一，因为后续要用 taskIdentifiers 来作为 key 对应的 delegate
        dispatch_sync(url_session_manager_creation_queue(), block);
    } else {
        block();
    }
}

