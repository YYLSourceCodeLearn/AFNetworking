//被服务器重定向的时候调用
//此方法只会在 defaut session 或者 ephemeral session中调用，而在 background session中，session task 会自动重定向
- (void)URLSession:(NSURLSession *)session
              task:(NSURLSessionTask *)task
willPerformHTTPRedirection:(NSHTTPURLResponse *)response
        newRequest:(NSURLRequest *)request
 completionHandler:(void (^)(NSURLRequest *))completionHandler
{
    NSURLRequest *redirectRequest = request;

    //看是否有对应的 user block 有的话转发出去，通过这4个参数，返回一个 NSURLRequest类型参数，request 转发、网络重定向
    if (self.taskWillPerformHTTPRedirection) {
        //用自己自定义的一个重定向的 block 实现，返回一个新的 request
        redirectRequest = self.taskWillPerformHTTPRedirection(session, task, response, request);
    }

    if (completionHandler) {
        //用 request 重新请求
        completionHandler(redirectRequest);
    }
}

//https 认证
//这个是 non-session-level 级别的认证
- (void)URLSession:(NSURLSession *)session
              task:(NSURLSessionTask *)task
didReceiveChallenge:(NSURLAuthenticationChallenge *)challenge
 completionHandler:(void (^)(NSURLSessionAuthChallengeDisposition disposition, NSURLCredential *credential))completionHandler
{
    NSURLSessionAuthChallengeDisposition disposition = NSURLSessionAuthChallengePerformDefaultHandling;
    __block NSURLCredential *credential = nil;

    if (self.taskDidReceiveAuthenticationChallenge) {
        disposition = self.taskDidReceiveAuthenticationChallenge(session, task, challenge, &credential);
    } else {
        if ([challenge.protectionSpace.authenticationMethod isEqualToString:NSURLAuthenticationMethodServerTrust]) {
            if ([self.securityPolicy evaluateServerTrust:challenge.protectionSpace.serverTrust forDomain:challenge.protectionSpace.host]) {
                disposition = NSURLSessionAuthChallengeUseCredential;
                credential = [NSURLCredential credentialForTrust:challenge.protectionSpace.serverTrust];
            } else {
                disposition = NSURLSessionAuthChallengeCancelAuthenticationChallenge;
            }
        } else {
            disposition = NSURLSessionAuthChallengePerformDefaultHandling;
        }
    }

    if (completionHandler) {
        completionHandler(disposition, credential);
    }
}

//当一个 session task 需要发送一个新的 request body stream 到服务器的时候，调用该代理方法

/*
 该代理方法会在下面两种情况下调用
 1.如果 task 是由 uploadTaskWithStreamRequest：创建的，那么提供初始的 request body stream 时候会调用该代理方法
 2.因为认证挑战或者其它可恢复的服务器错误，而导致需要客户端重新发送一个含有 body stream 的 request 这时候会调用该代理
 
 */
- (void)URLSession:(NSURLSession *)session
              task:(NSURLSessionTask *)task
 needNewBodyStream:(void (^)(NSInputStream *bodyStream))completionHandler
{
    NSInputStream *inputStream = nil;
    //有自定义的 taskNeedNewBodyStream,用自定义的，不然用 task 里原始的 stream
    if (self.taskNeedNewBodyStream) {
        inputStream = self.taskNeedNewBodyStream(session, task);
    } else if (task.originalRequest.HTTPBodyStream && [task.originalRequest.HTTPBodyStream conformsToProtocol:@protocol(NSCopying)]) {
        inputStream = [task.originalRequest.HTTPBodyStream copy];
    }

    if (completionHandler) {
        completionHandler(inputStream);
    }
}

//周期性的通知代理发送到服务器端的数据进度
/*
 就是每次发送数据给服务器，会调用这个方法，通知已经发送了多少，总共要发送多少
 代理方法里也就是仅仅调用了我们自定义的 Block 而已
 */
- (void)URLSession:(NSURLSession *)session
              task:(NSURLSessionTask *)task
   didSendBodyData:(int64_t)bytesSent
    totalBytesSent:(int64_t)totalBytesSent
totalBytesExpectedToSend:(int64_t)totalBytesExpectedToSend
{

    //如果totalUnitCount 获取失败，就是用 HTTP header 中的 Content-Length 作为totalUnitCount
    
    int64_t totalUnitCount = totalBytesExpectedToSend;
    if(totalUnitCount == NSURLSessionTransferSizeUnknown) {
        NSString *contentLength = [task.originalRequest valueForHTTPHeaderField:@"Content-Length"];
        if(contentLength) {
            totalUnitCount = (int64_t) [contentLength longLongValue];
        }
    }
    
    AFURLSessionManagerTaskDelegate *delegate = [self delegateForTask:task];
    
    if (delegate) {
        [delegate URLSession:session task:task didSendBodyData:bytesSent totalBytesSent:totalBytesSent totalBytesExpectedToSend:totalBytesExpectedToSend];
    }

    if (self.taskDidSendBodyData) {
        self.taskDidSendBodyData(session, task, bytesSent, totalBytesSent, totalUnitCount);
    }
}

/*
 task 完成以后的回调，成功和失败都会回调到这里
 函数讨论：
 注意这里的 error 不会报告服务器端的 error，他表示的是客户端这边的 error，比如无法解析 hostname 或者连不上 host 主机
 */
- (void)URLSession:(NSURLSession *)session
              task:(NSURLSessionTask *)task
didCompleteWithError:(NSError *)error
{
    //根据 task 去取我们一开始创建绑定的 delegate
    AFURLSessionManagerTaskDelegate *delegate = [self delegateForTask:task];

    // delegate may be nil when completing a task in the background
    if (delegate) {
        //把代理转发给我们绑定的 delegate
        [delegate URLSession:session task:task didCompleteWithError:error];
        //转发完移除 delegate
        [self removeDelegateForTask:task];
    }
    
    //自定义 Block 回调
    if (self.taskDidComplete) {
        self.taskDidComplete(session, task, error);
    }
}

#pragma mark - NSURLSessionDataDelegate

//收到服务器响应后的调用
/*
 函数作用：
 告诉代理，该 data task 获取到服务器端传回的最初始回复（response）
 completionHandler这个 block，通过传入一个类型为NSURLSessionResponseDisposition的变量来决定该传输任务接下来该做什么
 NSURLSessionResponseCancel            //该 task 会被取消
 NSURLSessionResponseAllow             //该 task 正常进行
 NSURLSessionResponseBecomeDownload    //会调用 URLSession:dataTask:didBecomeDownloadTask 方法来新建一个 download task 以代替当前的 data task
 NSURLSessionResponseBecomeStream      //转成一个 StreamTask
 */

/*
 该方法是可选的，除非你必须支持“multipart/x-mixed-replace”类型的 content-type。因为如果你的 request 中包含了这种类型的 content-type，服务器会将数据分片返回，而且每次返回来的数据会覆盖之前的数据。每次返回新的数据时，session 都会调用该函数，你应该在这个函数中合理地处理先前的数据，否则会被新数据覆盖。如果你没有提供该方法的实现，那么 session 将会继续任务，也就是会覆盖之前的数据
*/
- (void)URLSession:(NSURLSession *)session
          dataTask:(NSURLSessionDataTask *)dataTask
didReceiveResponse:(NSURLResponse *)response
 completionHandler:(void (^)(NSURLSessionResponseDisposition disposition))completionHandler
{

    //设置默认为继续进行
    NSURLSessionResponseDisposition disposition = NSURLSessionResponseAllow;

    //自定义去设置
    if (self.dataTaskDidReceiveResponse) {
        disposition = self.dataTaskDidReceiveResponse(session, dataTask, response);
    }

    if (completionHandler) {
        completionHandler(disposition);
    }
}

//上面的代理如果设置为 NSURLSessionResponseBecomeDownload，则会调用这个方法
- (void)URLSession:(NSURLSession *)session
          dataTask:(NSURLSessionDataTask *)dataTask
didBecomeDownloadTask:(NSURLSessionDownloadTask *)downloadTask
{
    //因为转变了 task，所以要对 task 做一个重新绑定
    AFURLSessionManagerTaskDelegate *delegate = [self delegateForTask:dataTask];
    if (delegate) {
        [self removeDelegateForTask:dataTask];
        [self setDelegate:delegate forTask:downloadTask];
    }
    //执行自定义的 block
    if (self.dataTaskDidBecomeDownloadTask) {
        self.dataTaskDidBecomeDownloadTask(session, dataTask, downloadTask);
    }
}

//当我们获取到数据就会调用，会被反复调用，请求到的数据就在这被拼装完整
- (void)URLSession:(NSURLSession *)session
          dataTask:(NSURLSessionDataTask *)dataTask
    didReceiveData:(NSData *)data
{

    AFURLSessionManagerTaskDelegate *delegate = [self delegateForTask:dataTask];
    [delegate URLSession:session dataTask:dataTask didReceiveData:data];

    if (self.dataTaskDidReceiveData) {
        self.dataTaskDidReceiveData(session, dataTask, data);
    }
}

//当 task 接收到所有期望的数据后，session 会调用次代理方法
/*
 函数作用：
 询问 data task 或上传任务（upload task）是否缓存 response
 
 函数讨论：
 当 task 接收到所有期望的数据后，session 会调用此代理方法。如果你没有实现该方法，那么就会使用创建 session时使用的 configuration 对象决定缓存策略。这个代理方法最初的目的是为了阻止缓存特定的 URLs 或者修改 NSCacheURLResponse 对象相关的 userInfo字典
 该方法只会当 request 决定缓存 resposne 时候调用，作为准则，reposne 只会当以下都成立的时候返回缓存:
 该 request 是 HTTP 或 HTTPS 的 URL请求（或者你自定义的网络协议，并且确保该协议支持缓存）
 确保 request 请求是成功的（返回 status code 为200-299）
 返回的 response 是来自服务器端的，而非缓存中本省就有的
 
 提供的 NSURLRequest 对象的缓存策略要允许进行缓存
 服务器返回的 response中与缓存相关的 header 要允许缓存
 该 response 的大小不能比提供的缓存控件大太多（比如你提供了一个磁盘缓存，那么 resposne 大小一定不能比磁盘缓存控件还要大5%）
 */
- (void)URLSession:(NSURLSession *)session
          dataTask:(NSURLSessionDataTask *)dataTask
 willCacheResponse:(NSCachedURLResponse *)proposedResponse
 completionHandler:(void (^)(NSCachedURLResponse *cachedResponse))completionHandler
{
    NSCachedURLResponse *cachedResponse = proposedResponse;

    if (self.dataTaskWillCacheResponse) {
        cachedResponse = self.dataTaskWillCacheResponse(session, dataTask, proposedResponse);
    }

    if (completionHandler) {
        completionHandler(cachedResponse);
    }
}

//当 session 中所有已经入队的消息被发送出去后，会调用该代理方法
- (void)URLSessionDidFinishEventsForBackgroundURLSession:(NSURLSession *)session {
    if (self.didFinishEventsForBackgroundURLSession) {
        dispatch_async(dispatch_get_main_queue(), ^{
            self.didFinishEventsForBackgroundURLSession(session);
        });
    }
}

#pragma mark - NSURLSessionDownloadDelegate

//下载完成的时候调用
- (void)URLSession:(NSURLSession *)session
      downloadTask:(NSURLSessionDownloadTask *)downloadTask
didFinishDownloadingToURL:(NSURL *)location
{
    AFURLSessionManagerTaskDelegate *delegate = [self delegateForTask:downloadTask];
    //这个是 session 的，也就是全局的，后面的个人代理也会做同样的这件事
    if (self.downloadTaskDidFinishDownloading) {
        //调用自定的 block 拿到文件存储的地址
        NSURL *fileURL = self.downloadTaskDidFinishDownloading(session, downloadTask, location);
        if (fileURL) {
            delegate.downloadFileURL = fileURL;
            NSError *error = nil;
            //从临时的下载路径移动至我们需要的路径
            if (![[NSFileManager defaultManager] moveItemAtURL:location toURL:fileURL error:&error]) {
                [[NSNotificationCenter defaultCenter] postNotificationName:AFURLSessionDownloadTaskDidFailToMoveFileNotification object:downloadTask userInfo:error.userInfo];
            }

            return;
        }
    }
    //转发代理
    if (delegate) {
        [delegate URLSession:session downloadTask:downloadTask didFinishDownloadingToURL:location];
    }
}

